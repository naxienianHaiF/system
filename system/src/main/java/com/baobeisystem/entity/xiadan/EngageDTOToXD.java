public class EngageDTOToXD {

	private String taxpayerType;
	private String invoiceType;
	private String taxNumber;
	private String taxRegistAddress;
	private String bank;
	private String account;
	
	public EngageDTOToXD(){
		
	}
	
	public String getTaxpayerType() {
		return taxpayerType;
	}
	public void setTaxpayerType(String taxpayerType) {
		this.taxpayerType = taxpayerType;
	}
	public String getInvoiceType() {
		return invoiceType;
	}
	public void setInvoiceType(String invoiceType) {
		this.invoiceType = invoiceType;
	}
	public String getTaxNumber() {
		return taxNumber;
	}
	public void setTaxNumber(String taxNumber) {
		this.taxNumber = taxNumber;
	}
	public String getTaxRegistAddress() {
		return taxRegistAddress;
	}
	public void setTaxRegistAddress(String taxRegistAddress) {
		this.taxRegistAddress = taxRegistAddress;
	}
	public String getBank() {
		return bank;
	}
	public void setBank(String bank) {
		this.bank = bank;
	}
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}

	
	
}
